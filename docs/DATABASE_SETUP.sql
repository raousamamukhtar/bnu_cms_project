-- ============================================================================
-- BNU SUSTAINABILITY PROJECT - ORACLE 19c SETUP SCRIPT
-- ============================================================================
-- This script creates the core schema for the Sustainability Management System.
-- Run this script as the BNUCMS user or a user with DBA privileges.

-- 1. ROLES TABLE
CREATE TABLE ROLES (
    ROLE_ID NUMBER PRIMARY KEY,
    ROLE_NAME VARCHAR2(255) NOT NULL
);

-- 2. SCHOOLS TABLE
CREATE TABLE SCHOOLS (
    SCHOOL_ID NUMBER PRIMARY KEY,
    SCHOOL_NAME VARCHAR2(255) NOT NULL
);

-- 3. USERS TABLE
CREATE TABLE USERS (
    USER_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    FULL_NAME VARCHAR2(255) NOT NULL,
    EMAIL VARCHAR2(255) UNIQUE NOT NULL,
    PASSWORD VARCHAR2(255) NOT NULL,
    ROLE_ID NUMBER REFERENCES ROLES(ROLE_ID),
    SCHOOL_ID NUMBER REFERENCES SCHOOLS(SCHOOL_ID) NULL,
    REMEMBER_TOKEN VARCHAR2(100) NULL,
    EMAIL_VERIFIED_AT TIMESTAMP NULL,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- 4. SUSTAINABILITY_PERIOD TABLE
CREATE TABLE SUSTAINABILITY_PERIOD (
    PERIOD_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    DATA_MONTH NUMBER NOT NULL,
    DATA_YEAR NUMBER NOT NULL,
    STUDENTS NUMBER(10, 2) DEFAULT 0,
    EMPLOYEES NUMBER(10, 2) DEFAULT 0,
    CREATED_BY NUMBER REFERENCES USERS(USER_ID),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- 5. PAPER_CONSUMPTION
CREATE TABLE PAPER_CONSUMPTION (
    PAPER_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PERIOD_ID NUMBER REFERENCES SUSTAINABILITY_PERIOD(PERIOD_ID) ON DELETE CASCADE,
    PAPER_REAMS NUMBER(10, 2) DEFAULT 0,
    SHEETS_PER_REAM NUMBER(10, 2) DEFAULT 500,
    PER_CAPITA_REAM NUMBER(10, 4) DEFAULT 0
);

-- 6. ELECTRICITY_CONSUMPTION
CREATE TABLE ELECTRICITY_CONSUMPTION (
    ELECTRICITY_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PERIOD_ID NUMBER REFERENCES SUSTAINABILITY_PERIOD(PERIOD_ID) ON DELETE CASCADE,
    UNITS_KWH NUMBER(10, 2) DEFAULT 0,
    TOTAL_COST NUMBER(10, 2) DEFAULT 0,
    KWH_SOLAR_OFFSET NUMBER(10, 2) DEFAULT 0
);

-- 7. WASTE_GENERATION
CREATE TABLE WASTE_GENERATION (
    WASTE_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PERIOD_ID NUMBER REFERENCES SUSTAINABILITY_PERIOD(PERIOD_ID) ON DELETE CASCADE,
    ORGANIC_KG NUMBER(10, 2) DEFAULT 0,
    RECYCLABLE_KG NUMBER(10, 2) DEFAULT 0,
    OTHER_KG NUMBER(10, 2) DEFAULT 0,
    TOTAL_WASTE NUMBER(10, 2) DEFAULT 0,
    PER_CAPITA_WASTE NUMBER(10, 4) DEFAULT 0
);

-- 8. GENERATOR_USAGE
CREATE TABLE GENERATOR_USAGE (
    GENERATOR_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PERIOD_ID NUMBER REFERENCES SUSTAINABILITY_PERIOD(PERIOD_ID) ON DELETE CASCADE,
    TOTAL_HOURS NUMBER(10, 2) DEFAULT 0,
    DIESEL_LITRES NUMBER(10, 2) DEFAULT 0,
    FUEL_TYPE VARCHAR2(50) DEFAULT 'Diesel',
    COST NUMBER(10, 2) DEFAULT 0,
    AVG_RUNNING_HOURS NUMBER(10, 2) NULL, -- Legacy
    FUEL_LITRES NUMBER(10, 2) NULL -- Legacy
);

-- 9. WATER_CONSUMPTION
CREATE TABLE WATER_CONSUMPTION (
    WATER_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PERIOD_ID NUMBER REFERENCES SUSTAINABILITY_PERIOD(PERIOD_ID) ON DELETE CASCADE,
    UNITS_M3 NUMBER(10, 2) DEFAULT 0,
    COST NUMBER(10, 2) DEFAULT 0
);

-- 10. CARBON_METRICS
CREATE TABLE CARBON_METRICS (
    CARBON_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PERIOD_ID NUMBER REFERENCES SUSTAINABILITY_PERIOD(PERIOD_ID) ON DELETE CASCADE,
    AQI_SCORE NUMBER(10, 2) DEFAULT 0,
    CARBON_FOOTPRINT NUMBER(10, 2) DEFAULT 0
);

-- 11. EVENTS TABLE
CREATE TABLE EVENTS (
    EVENT_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    SCHOOL_ID NUMBER REFERENCES SCHOOLS(SCHOOL_ID) ON DELETE SET NULL,
    EVENT_MONTH NUMBER NOT NULL,
    EVENT_YEAR NUMBER NOT NULL,
    EVENT_NAME VARCHAR2(255) NOT NULL,
    EVENT_TYPE VARCHAR2(100) NOT NULL,
    EVENT_DATE DATE NOT NULL,
    DESCRIPTION CLOB NOT NULL,
    EVENT_LINK VARCHAR2(500) NULL,
    ENTERED_BY NUMBER REFERENCES USERS(USER_ID) ON DELETE CASCADE,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

